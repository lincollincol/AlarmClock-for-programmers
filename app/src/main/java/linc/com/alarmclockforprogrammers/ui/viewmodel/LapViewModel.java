package linc.com.alarmclockforprogrammers.ui.viewmodel;

public class LapViewModel {

    private int id;
    private String time;

    public void setId(int id) {
        this.id = id;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public String getTime() {
        return time;
    }
}
